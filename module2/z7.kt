package module2
/*
Задание №7
Тест
Напишите тест на любую тему. Пользователь должен вводить номер варианта из предложенных ответов.
Придумайте минимум 5 вопросов. Вывод результата тестирования должен происходить через оператор when, в виде текстового комментария (пример: «Ваши знания истории на крепкую четверку»)


*/


fun main() {
    print("1. Какой город является столицей Франции? \n1 - Лондон\n2 - Берлин\n3 - Париж\nВведите номер ответа: ")
    val answer1 = readLine()?.toIntOrNull() ?: 0

    print("2. Сколько будет 5 + 7? \n1 - 10\n2 - 12\n3 - 15\nВведите номер ответа: ")
    val answer2 = readLine()?.toIntOrNull() ?: 0

    print("3. Как называется самая длинная река в мире? \n1 - Нил\n2 - Амазонка\n3 - Волга\nВведите номер ответа: ")
    val answer3 = readLine()?.toIntOrNull() ?: 0

    print("4. Какое животное самое быстрое на суше? \n1 - Гепард\n2 - Лошадь\n3 - Тигр\nВведите номер ответа: ")
    val answer4 = readLine()?.toIntOrNull() ?: 0

    print("5. Сколько планет в Солнечной системе? \n1 - 7\n2 - 8\n3 - 9\nВведите номер ответа: ")
    val answer5 = readLine()?.toIntOrNull() ?: 0

    val score = listOf(answer1, answer2, answer3, answer4, answer5).count { it == listOf(3, 2, 1, 1, 2)[it - 1] }

    when (score) {
        5 -> println("Отлично! Вы обладаете широкими знаниями.")
        4 -> println("Очень хорошо! Но есть небольшие пробелы.")
        3 -> println("Неплохо, но можно подтянуть знания.")
        2 -> println("Стоит повторить основные факты.")
        else -> println("Похоже, вам стоит больше читать и учить новое!")
    }
}
